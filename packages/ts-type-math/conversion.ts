export namespace Convert {
  export namespace U8Hex {
  }

  export namespace U8Decimal {
    export type ToU8Binary<
      decimal extends U8Decimal
    > = Satisfies<U8Binary,
      Catalog.U8DecimalToU8Binary[decimal]
    >

    export type ToAscii<
      ascii extends U8Decimal
    > = Satisfies<Ascii,
      Catalog.U8DecimalToAscii[ascii]
    >
  }

  export namespace U8Binary {
    export type ToU8Decimal<
      binary extends U8Binary
    > = Satisfies<U8Decimal,
      Catalog.U8BinaryToU8Decimal[binary]
    >

    export type ToAscii<
      ascii extends U8Binary
    > = Satisfies<Ascii,
      Catalog.U8BinaryToAscii[ascii]
    >
  }

  export namespace U32Decimal {
    export type ToU32Binary<
      decimal extends number
    > = Satisfies<WasmValue,
      To32Binary<decimal>
    >
  }

  export namespace U32Binary {
    export type ToU32Decimal<
      binary extends U32Binary
    > = Satisfies<number,
      ToDecimalSigned<binary>
    >
  }

  export namespace U64Decimal {
    export type ToU64Binary<
      decimal extends bigint
    > = Satisfies<WasmValue,
      To64Binary<decimal>
    >
  }

  export namespace Ascii {
    export type ToU8Decimal<
      ascii extends Ascii
    > = Satisfies<U8Decimal,
      Catalog.AsciiToU8Decimal[ascii]
    >

    export type ToU8Binary<
      ascii extends Ascii
    > = Satisfies<U8Binary,
      Catalog.AsciiToU8Binary[ascii]
    >

    export type ToU32Binary<
      ascii extends Ascii
    > = Satisfies<WasmValue,
      Pad.StartWithZeros<
        Convert.U8Decimal.ToU8Binary<
          Convert.Ascii.ToU8Decimal<ascii>
        >,
        32
      >
    >

    export type ToWasmValue<
      ascii extends Ascii,
    > = Satisfies<WasmValue,
      ToU32Binary<ascii>
    >
  }

  export namespace TSNumber {
    export type ToWasmValue<
      value extends number,
      wasmType extends 'i32' | 'f32' | 'f64',
    > = Satisfies<WasmValue,
      wasmType extends 'i32' ? Convert.U32Decimal.ToU32Binary<value> :
      wasmType extends 'f32' ? never : // TODO
      wasmType extends 'f64' ? never : // TODO
      never
    >
  }

  export namespace TSBigInt {
    export type ToWasmValue<
      value extends bigint,
    > = Satisfies<WasmValue,
      Convert.U64Decimal.ToU64Binary<value>
    >
  }

  export namespace WasmValue {
    export type ToTSNumber<
      value extends WasmValue,
      wasmType extends WasmType
    > = Satisfies<number,
      wasmType extends 'i32' ? Convert.U32Binary.ToU32Decimal<value> :
      wasmType extends 'i64' ? Convert.U32Binary.ToU32Decimal<value> : // TODO
      wasmType extends 'f32' ? never : // TODO
      wasmType extends 'f64' ? never : // TODO
      never
    >
  }
}

